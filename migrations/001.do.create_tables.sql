

CREATE TABLE folder (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  uuid UUID NOT NULL,
  folder_name TEXT NOT NULL

);


CREATE TABLE note (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  folder_id INTEGER REFERENCES folder(id),
  folder_uuid UUID NOT NULL,
  uuid UUID NOT NULL,
  note_name TEXT DEFAULT 0,
  modified DATE,
  content TEXT
);




